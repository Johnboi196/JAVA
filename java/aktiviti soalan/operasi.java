public class operasi {
    public static void main (String[]args){
        //diffrent of datatype can change 
        int nom1,nom2;
        int op1,op2,op3,op4,op5;

        //calculate value conclulations

        nom1=15;
        nom2=2;


        //operasi aritmetik
        op1=nom1+nom2;
        op2=nom1-nom2;
        op3=nom1*nom2;
        op4=nom1/nom2;
        op5=nom1%nom2;

        //paparan output
        System .out .println("Hasil tambah ialah:"+nom1+'+' +nom2+'='+op1);
        System .out .println("Hasil tolak ialah:"+nom1+'+' +nom2+'='+op2);    
        System .out .println("Hasil darab ialah:"+nom1+'+' +nom2+'='+op3); 
        System .out .println("Hasil bahagi ialah:"+nom1+'+' +nom2+'='+op4);
        System .out .println("Hasil modulus ialah:"+nom1+'+' +nom2+'='+op5);   
    }
}
